
public class Variables {

	public static void main(String[] args) {
		
		int edad = 35; //Declaración e iniciación de variable en una única línea
		
		/* la siguiente instrución imprime en la consola (System.out)
		 * el valor de la variable edad
		 */
		System.out.println(edad);
		
		edad = 75;
		System.out.println(edad);
	}

}
